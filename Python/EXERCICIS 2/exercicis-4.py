#4.  Donats dos nombres, mostrar la suma, resta, divisió i multiplicació de tots dos.

numero1 = int(input("Introduce el primer numero: "))
numero2 = int(input("Introduce el segundo numero: "))

suma = numero1 + numero2
resta = numero1 - numero2
divisio = numero1 / numero2
multiplicacio = numero1 * numero2

print("resultado suma: " + str(suma))
print("resultado resta: " + str(resta))
print("resultado divisio: " + str(divisio))
print("resultado multiplicacio: " + str(multiplicacio))