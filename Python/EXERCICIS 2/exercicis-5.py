# 5.Escriure un programa que converteixi un valor donat en graus Fahrenheit a graus Celsius. 
# Recordeu que la fórmula per a la conversió és:
# C = (F-32) * 5/9

Fahrenheit = int(input("un valor donat en graus Fahrenheit: "))

Celsius  = (Fahrenheit-32) * 5/9

print("resultado graus Celsius: " + str(Celsius))